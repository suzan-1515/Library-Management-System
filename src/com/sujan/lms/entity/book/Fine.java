package com.sujan.lms.entity.book;

/**
 *
 * @author Suzn
 */
public class Fine {
    
    private int id;
    private float amount;

    public Fine() {
        amount = 0f;
    }
    
    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the amount
     */
    public float getAmount() {
        return amount;
    }

    /**
     * @param amount the amount to set
     */
    public void setAmount(float amount) {
        this.amount = amount;
    }
    
}
