    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (validation.isSubjectFormValid(titleTextField.getText())) {

            subject.setTitle(titleTextField.getText());

            try {
                SubjectBLL.updateSubject(subject);
                notifyDataSetChanged(subject);
                Alert.showInformation(this, "Subject updated successfully!");

                resetFields();
                this.dispose();
            } catch (RecordNotFoundException | MissingFileException | CorruptedDataException ex) {
                Logy.e(ex);
                Alert.showError(this, ex.getMessage());
            }

        }
    }//GEN-LAST:event_updateButtonActionPerformed

